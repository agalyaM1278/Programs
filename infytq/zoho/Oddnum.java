package zoho;

public class Oddnum {
    public static void main(String args[]){
        int m=2;
        int n=12;
        if(m%2!=0){
            for(int i=m;i<=n;i+=2){
                System.out.print(i+" ");
            }
        }else{
        for(int i=m+1;i<=n;i+=2){
            
                System.out.print(i+" ");
          
        }
    }
    }
}
